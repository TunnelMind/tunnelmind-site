#!/usr/bin/env node
// Generate supabase/migrations/006_oai_seed.sql by aggregating
// public/trackers.json into top-N OAI registry seed entries.
//
// Deterministic: same input → same output. Re-run if trackers.json changes.
// Seeds are inserted with status='reserved' (not active) because seed
// entries have no attestations yet — they're known to exist but not yet
// observed by an attested sensor. They auto-promote to 'active' on first
// signed observation.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// Default TOP_N is 200 (full seed). Override via env to seed a subset.
const TOP_N = parseInt(process.env.TOP_N || '200', 10);
const TRACKERS_PATH = path.join(__dirname, '..', 'public', 'trackers.json');
const OUT_PATH = path.join(__dirname, '..', 'supabase', 'migrations', '006_oai_seed.sql');

// Industry segments that are in-scope per OAI Standard Section 3.
// Out-of-scope (unknown, media, cdn, infrastructure, gaming, ecommerce,
// finance, messaging, security) are not registered to avoid the
// "do not invent entities" foot-gun.
const I_TO_CATEGORY = {
  ad_tech:        'tracker.pixel.advertising',
  marketing_tech: 'tracker.pixel.marketing',
  analytics:      'tracker.pixel.analytics',
  fingerprinting: 'tracker.fingerprinter',
  data_broker:    'data_broker',
  social:         'tracker.pixel.social',
};

const trackers = JSON.parse(fs.readFileSync(TRACKERS_PATH, 'utf8'));

// ── Aggregate domains by company ──────────────────────────────────

const byCompany = new Map();
for (const [domain, entry] of Object.entries(trackers)) {
  if (!I_TO_CATEGORY[entry.i]) continue;
  const company = (entry.c || '').trim();
  if (!company) continue;
  let agg = byCompany.get(company);
  if (!agg) {
    agg = { name: company, domains: [], industries: {}, max_cpm: 0 };
    byCompany.set(company, agg);
  }
  agg.domains.push(domain);
  agg.industries[entry.i] = (agg.industries[entry.i] || 0) + 1;
  if (typeof entry.cpm === 'number' && entry.cpm > agg.max_cpm) {
    agg.max_cpm = entry.cpm;
  }
}

// ── Rank ──────────────────────────────────────────────────────────
// Score = in_scope_domain_count * max_cpm. Tiebreak alphabetically
// by company name for determinism.

const ranked = [...byCompany.values()]
  .map(c => ({
    ...c,
    score: c.domains.length * c.max_cpm,
    dominant_i: Object.entries(c.industries).sort((a, b) => b[1] - a[1])[0][0],
  }))
  .sort((a, b) => (b.score - a.score) || a.name.localeCompare(b.name))
  .slice(0, TOP_N);

console.error(`In-scope companies: ${byCompany.size}; seeding top ${ranked.length}.`);

// ── Slug generation (deduped) ─────────────────────────────────────

const usedSlugs = new Set();
function makeSlug(name) {
  let s = name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  if (!s || !/^[a-z0-9]/.test(s)) return null;
  let candidate = s;
  let n = 2;
  while (usedSlugs.has(candidate)) {
    candidate = `${s}-${n++}`;
  }
  usedSlugs.add(candidate);
  return candidate;
}

// ── SQL emission ──────────────────────────────────────────────────

const sqlEscape = s => "'" + String(s).replace(/'/g, "''") + "'";

const lines = [];
lines.push(`-- Migration 006: OAI registry seed — top ${TOP_N} entities`);
lines.push(`-- Generated by scripts/generate-oai-seed.js from public/trackers.json`);
lines.push(`-- Re-run the generator if trackers.json changes. Hand-edits will be lost.`);
lines.push(`--`);
lines.push(`-- Seed entries are inserted with status='reserved'. They are NOT publicly`);
lines.push(`-- resolvable (RLS) and have no attestations. First signed observation by an`);
lines.push(`-- attested sensor (Familiar / Augur) promotes the entry to 'active' and`);
lines.push(`-- appends the attestation.`);
lines.push(``);
lines.push(`-- Idempotency: re-running this migration on a populated registry would`);
lines.push(`-- issue NEW OAI IDs (sequence advances), creating duplicates. The guard`);
lines.push(`-- below short-circuits if any seed-shaped reserved rows already exist.`);
lines.push(``);
lines.push(`DO $$`);
lines.push(`DECLARE`);
lines.push(`  v_existing int;`);
lines.push(`  v_new_id   text;`);
lines.push(`BEGIN`);
lines.push(`  SELECT count(*) INTO v_existing FROM oai_registry WHERE status = 'reserved';`);
lines.push(`  IF v_existing > 0 THEN`);
lines.push(`    RAISE NOTICE 'oai_registry already contains % reserved row(s); seed skipped.', v_existing;`);
lines.push(`    RETURN;`);
lines.push(`  END IF;`);
lines.push(``);

for (const c of ranked) {
  const slug = makeSlug(c.name);
  if (!slug) {
    console.error(`Skipping unsluggable company: ${JSON.stringify(c.name)}`);
    continue;
  }
  const category = I_TO_CATEGORY[c.dominant_i];
  const domainsArr = c.domains
    .slice()
    .sort()
    .map(d => sqlEscape(d))
    .join(', ');
  const aliasLit = sqlEscape(`oai:${slug}`);
  const nameLit = sqlEscape(c.name);
  const categoryLit = sqlEscape(category);

  // Use to_jsonb(ARRAY[...]::text[]) for arrays because jsonb_build_array
  // has a 100-argument cap in Postgres, and some tracker companies have
  // hundreds of domains.
  lines.push(`  v_new_id := issue_oai(${categoryLit});`);
  lines.push(`  INSERT INTO oai_registry (oai_id, aliases, record, status) VALUES (`);
  lines.push(`    v_new_id,`);
  lines.push(`    ARRAY[${aliasLit}]::text[],`);
  lines.push(`    jsonb_build_object(`);
  lines.push(`      '@context', 'https://tunnelmind.ai/oai/context.jsonld',`);
  lines.push(`      '@type', 'ObservedActor',`);
  lines.push(`      'id', v_new_id,`);
  lines.push(`      'aliases', to_jsonb(ARRAY[${aliasLit}]::text[]),`);
  lines.push(`      'name', ${nameLit},`);
  lines.push(`      'category', ${categoryLit},`);
  lines.push(`      'domains', to_jsonb(ARRAY[${domainsArr}]::text[]),`);
  lines.push(`      'status', 'reserved',`);
  lines.push(`      'schema_version', '1.0',`);
  lines.push(`      'issued_at', to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD"T"HH24:MI:SS"Z"')`);
  lines.push(`    ),`);
  lines.push(`    'reserved'`);
  lines.push(`  );`);
  lines.push(``);
}

lines.push(`  RAISE NOTICE 'OAI seed complete: % entries issued.', ${ranked.length};`);
lines.push(`END $$;`);

fs.writeFileSync(OUT_PATH, lines.join('\n') + '\n');
console.error(`Wrote ${OUT_PATH} (${lines.length} lines).`);
